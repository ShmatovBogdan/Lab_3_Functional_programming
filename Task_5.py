# Напишіть функцію push(stack, item), яка імітує операцію “додавання в стек”, 
# повертаючи нову копію списку з новим елементом на початку. Початковий список (стек) 
# не змінюється.

def push(stack: list, item) -> list:
    return [item] + stack

s1 = [3, 2, 1]
s2 = push(s1, 4)

print(s2)
print(s1)
